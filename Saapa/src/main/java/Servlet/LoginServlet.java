    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String login = request.getParameter("cpf");
        String pwd = request.getParameter("senha");
        HttpSession session = request.getSession();
        Usuario usuario = new Usuario();
        UsuarioCtrl usuarioCtrl = new UsuarioCtrl();
        String permissao;
        try {
            permissao = usuarioCtrl.autenticar(login, pwd);

            if (permissao != null) {
                usuario = usuarioCtrl.selecionar(login, pwd);

                session.setAttribute("usuarioid", usuario.getUsuarioId());
                session.setAttribute("permissao", permissao);

                session.setMaxInactiveInterval(7200);
                response.sendRedirect("index.jsp");
            } else {

                response.sendRedirect("erro.jsp");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

